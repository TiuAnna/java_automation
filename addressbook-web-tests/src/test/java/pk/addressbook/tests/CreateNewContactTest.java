package pk.addressbook.tests;// Generated by Selenium IDE
import org.junit.Test;
import org.testng.Assert;
import pk.addressbook.model.ContactData;
import pk.addressbook.model.GroupData;

import java.util.ArrayList;
import java.util.Comparator;

public class CreateNewContactTest extends TestBase{
  @Test
  public void createNewContact() {
    app.getNavigationHelper().goToHomePage();
    ArrayList<ContactData> before = app.getContactHelper().getContactList();
    app.getContactHelper().goToNewContactCreationPage();
    ContactData contact = new ContactData("Nick", "Yellow",
            "LA", null, "thesecond@mail.com", app.getContactHelper().getGroupName());
    app.getContactHelper().fillTheContactForm(contact, true);
    app.getContactHelper().submitNewContactCreation();
    app.getContactHelper().returnToContactPage();
    ArrayList<ContactData> after = app.getContactHelper().getContactList();
    Assert.assertEquals(after.size(), before.size() + 1);

    contact.setId(after.stream().max((Comparator.comparingInt(ContactData::id))).get().id());
    before.add(contact);
    Comparator<ContactData> byId = Comparator.comparingInt(ContactData::id);
    before.sort(byId);
    after.sort(byId);
    Assert.assertEquals(before, after);
    
  }

}
